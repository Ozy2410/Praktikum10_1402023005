    private void jButtonDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDaftarActionPerformed
        // TODO add your handling code here:
        if(!jCheckBoxSetuju.isSelected()){
            JOptionPane.showMessageDialog(this, "Mohon Check Persutujuan Peraturan",
               "Belum Setuju", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!checkCaptcha(jLabelCaptcha.getText(), jTextFieldCaptcha.getText())){
            JOptionPane.showMessageDialog(this, "Ada Kesalahan di Captcha, Mohon Periksa Ulang Captcha",
               "Captcha Tidak Sesuai", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String nama_depan = jTextFieldNamaDepan.getText();
        String nama_belakang = jTextFieldNamaBelakang.getText();
        String jenis_kelamin = jRadioButtonPria.isSelected() ? "Pria" : "Wanita";
        String jenis_kendaraan = jComboBoxJenis.getSelectedItem().toString();
        
        String sql = "INSERT INTO table_ojol_yarsi (nama_depan, nama_belakang, jenis_kelamin, jenis_kendaraan) VALUES (?,?,?,?)";
        
        try (Connection c = Koneksi.getConnection(); PreparedStatement ps = c.prepareStatement(sql)) {
            ps.setString(1, nama_depan);
            ps.setString(2, nama_belakang);
            ps.setString(3, jenis_kelamin);
            ps.setString(4, jenis_kendaraan);
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Pendaftaran Berhasil");
            } else {
                JOptionPane.showMessageDialog(this, "Pendaftaran Gagal", "Failed", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Pendaftaran Gagal", "Failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDaftarActionPerformed
    
