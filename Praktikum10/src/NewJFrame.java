    private void jButtonListDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListDaftarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tableModel = new DefaultTableModel(new String[]{"ID", "Nama Depan", "Nama Belakang", "Jenis Kelamin", "Jenis Pengemudi"}, 0);
        JTable table = new JTable(tableModel);
        
        JPanel panel = new JPanel();
        JScrollPane scrollPane = new JScrollPane(table);
        panel.add(scrollPane);
        
        jDialog1.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        jDialog1.setContentPane(panel);
        jDialog1.pack();
        jDialog1.setVisible(true);
        
        try{
            if(conn == null || conn.isClosed()){
                conn = Koneksi.getConnection();
            }
            
            Statement stmt = conn.createStatement();
            ResultSet resultSet = stmt.executeQuery("SELECT * FROM table_ojol_yarsi");
            
            while(resultSet.next()){
                int id = resultSet.getInt("nomor");
                String nama_depan = resultSet.getString("nama_depan");
                String nama_belakang = resultSet.getString("nama_belakang");
                String jenis_kelamin = resultSet.getString("jenis_kelamin");
                String jenis_kendaraan = resultSet.getString("jenis_kendaraan");
                
                tableModel.addRow(new Object[] {id, nama_depan, nama_belakang, jenis_kelamin, jenis_kendaraan});
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    
    }//GEN-LAST:event_jButtonListDaftarActionPerformed
   
